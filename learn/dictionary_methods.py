# Dictionaries, in python are the exact equivalent of objects in javascript.
dic = {
    "name": "John Wick",
    "age": 43,
    "city": "New York",
}

# LENGTH of the dictionary
print(len(dic))
print('\n')

# Accessing the entries in the dictionary
print("for loop to access dictionary")
for entry in dic:
    print(f"{entry}: {dic[entry]}")

print('\n')

